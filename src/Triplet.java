public class Triplet {
    private String first, second;
    private int total;

    public Triplet(String first, String second, int total) {
        this.first = first;
        this.second = second;
        this.total = total;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }

    public int getTotal() {
        return total;
    }
}
